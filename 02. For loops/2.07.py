""" Write a program that uses exactly four for loops to print the sequence
of letters AAAAAAAAAABBBBBBBCDCDCDCDEFFFFFFG """

# to print 10 As
for i in range(10):
	print('A',end='')

# to print 7 Bs
for i in range(7):
	print('B',end='')

# to print 4 CDs
for i in range(4):
	print('CD',end='')


print('E',end='')

# to print 6 Es
for i in range(6):
	print('F',end='')

print('G')